    <p class="text-gray-700 max-w-3xl leading-relaxed">
     Jika mendengar kata Lamongan, mayoritas masyarakat akan teringat dengan makanan soto atau pecel lele Lamongan. Selain kedua kuliner tersebut, Lamongan merupakan kota destinasi wisata di Jawa Timur. 
        
    </p>
    <p class="mb-4">
   Ada beragam tempat wisata Lamongan  yang menarik untuk dikunjungi, mulai dari pantai, taman bermain, bukit, goa, wisata air, dan sebagainya.
  </p>
  <p class="mb-6">
  Berikut ini adalah beberapa tempat wisata yang wajib dikunjungi di Lamongan:
  </p>
  <h2 class="font-semibold mb-4 text-base">
   1. Wisata Bahari Lamongan (WBL)
  </h2>
  <img alt="Soto Lamongan" class="w-full rounded-lg mb-4 object-cover" style="height: 10%;" src="../../WebsiteOleh/img/wbll.jpg" width="600"/>
  <p class="text-xs text-gray-600 mb-6">
   Sumber foto pinterest.
  </p>
  <p>
  Wisata Bahari Lamongan atau disingkat WBL adalah tempat wisata bahari yang terletak di Kecamatan Paciran, Kabupaten Lamongan, Jawa Timur. Tempat wisata ini dibuka sejak 14 November 2004. Wisata Bahari Lamongan dikelola oleh PT Bumi Lamongan Sejati, sebuah perusahaan patungan Pemkab Lamongan dengan PT Bunga Wangsa Sejati. WBL menawarkan berbagai wahana permainan air, taman bermain, dan atraksi yang menarik bagi pengunjung dari segala usia.
  </p>
  <p class="mt-4">
   Beberapa wahana unggulan tempat wisata ini antara lain Istana Bawah Laut, Gua Insectarium, Space Shuttle, Anjungan Wali Songo, Texas City, Paus Dangdut, Tembak Ikan, Rumah Kaca, serta Istana Bajak Laut.
  </p>
  <p class="mt-4">
   Wisata Bahari Lamongan dibangun di lokasi yang dahulu dikenal dengan nama Pantai Tanjung Kodok.[2] Objek wisata ini berada di jalur pantai utara Surabaya-Tuban, serta berada di dekat sejumlah objek wisata andalan di Jawa Timur, di antaranya Gua Maharani, Makam dan Museum Sunan Drajat, Makam Sunan Sendang Duwur, dan Tanjung Kodok Resort. Tidak jauh dari WBL, sekitar 5 kilometer ke arah timur, sudah dioperasikan kawasan berikat yang dikenal dengan Lamongan Shorebase (LS). Sekitar 6 kilometer arah barat terdapat Pelabuhan Perikanan Nusantara di Kecamatan Brondong dengan tempat pelelangan ikan yang sangat dikenal di Jawa Timur.
  </p>
  <p class="text-xs text-gray-600 mb-6">
   Sumber wikipedia.
  </p>
  <h2 class="font-semibold mb-4 text-base">
   2. Pantai Tanjung Kodok
  </h2>
  <img alt="pantai tanjung kodok" class="w-full rounded-lg mb-4 object-cover" style="height: 10%;" src="../../WebsiteOleh/img/pantaikodok.jpeg" width="600"/>
  <p class="text-xs text-gray-600 mb-6">
   Sumber foto pinterest.
  </p>
  <p>
   Pantai Tanjung Kodok merupakan pantai populer di Lamongan yang berada di Jalan Raya Paciran, Kecamatan Paciran, Kabupaten Lamongan, Jawa Timur. Pantai Tanjung Kodok mempunyai keunikan batu karang menyerupai katak  atau kodok, selain itu banyak aktivitas wisata air yang bisa dicoba para wisatawan.
  </p>
  <p class="mt-4">
   Pantai Tanjung Kodok mempunyai panorama bebatuan karang dengan wahana seru dan resort untuk menginap.
  </p>
  <p class="mt-4">
   Harga tiket masuk Pantai Tanjung Kodok per orang Rp 100.000. Jam operasional Pantai Tanjung Kodok buka setiap hari selama 24 jam.
  </p>
    <p class="text-xs text-gray-600 mb-6">
    Sumber kompas.com
    </p>
  <h2 class="font-semibold mb-4 text-base">
   3. Goa Maharani
  </h2>
  <img alt="gambar goa maharani" class="w-full rounded-lg mb-4 object-cover" style="height: 10%;" src="../../WebsiteOleh/img/maharani.jpg" width="600"/>
  <p class="text-xs text-gray-600 mb-6">
   Sumber foto pinterest.
  </p>
  <p>
   Wisata Gua Maharani berada di Jl. Raya Paciran, Paciran, Kabupaten Lamongan, Jawa Timur. Awalnya, Gua Maharani ditemukan tanpa sengaja oleh Bupati Lamongan saat itu, yaitu Muhammad Faried pada tanggal 6 Agustus 1992. Gua ini terletak di dalam tanah dengan kedalaman 25 meter dari permukaan tanah dan luas rongga sekitar 2500  Karena memiliki keindahan berupa stalaktit dan stalakmit aktif yang menarik, maka gua tersebut dibuat sebagai tempat wisata dan telah resmi dibuka pada tanggal 10 Maret 1994.
  </p>
  <p class="mt-4">
   Selain itu, Mazoogo juga berisikan kebun binatang mini yang letaknya satu kompleks dengan Wisata Bahari Lamongan (WBL), tepatnya berada bersebrangan. Mazoogo menyediakan kebun binatang dengan berbagai fasilitas yang cocok dikunjungi sebagai sarana edukasi bagi anak-anak. Binatang-binatang yang ada tidak hanya dapat dilihat dari jauh, beberapa jenis binatang dapat diajak berfoto dengan pantauan pawangnya. Pengunjung juga dapat merasakan sensasi memberi makan binatang tertentu. Selain itu, terdapat museum satwa dan pertunjukan binatang yang akan menambah pengetahuan.
  </p>
    <p class="mt-4">
     Harga tiket masuk Gua Maharani per orang Rp 50.000. Jam operasional Gua Maharani buka setiap hari mulai pukul 08.00 hingga 17.00 WIB.
    </p>

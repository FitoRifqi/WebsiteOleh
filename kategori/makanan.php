  <h2 class="font-semibold mb-4 text-base">
   1. Oleh-oleh Khas Lamongan: Soto Lamongan
  </h2>
  <img alt="Soto Lamongan" class="w-full rounded-lg mb-4 object-cover" style="height: 10%;" src="../../WebsiteOleh/img/soto.jpg" width="600"/>
  <p class="text-xs text-gray-600 mb-6">
   Sumber foto pinterest.
  </p>
  <p>
  Soto Lamongan adalah makanan khas dari Lamongan, Jawa Timur, yang terkenal dengan kuahnya yang gurih dan berwarna kuning karena penggunaan kunyit dalam bumbu halus. Soto ini biasanya disajikan dengan irisan ayam, kol, tomat, daun bawang, mie bihun, dan telur rebus. Ciri khas lain dari Soto Lamongan adalah taburan koya udang yang membuatnya semakin lezat. 
  </p>
  <p class="mt-4">
   Penyajian Soto Lamongan:
  </p>
  <p class="mt-4">
   Soto Lamongan biasanya disajikan dengan berbagai pelengkap yang melengkapi cita rasa kuah dan daging ayam yang empuk. Selain irisan ayam, kol, tomat, daun bawang, mie bihun, dan telur rebus, Soto Lamongan juga sering disajikan dengan taburan bawang goreng, seledri, dan koya yang terbuat dari kerupuk dan kelapa parut.
  </p>
  <p class="text-xs text-gray-600 mb-6">
   Sumber lamongantourism.

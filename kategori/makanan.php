  <h2 class="font-semibold mb-4 text-base">
   2. Oleh-oleh Khas Lamongan: Nasi Boran
  </h2>
  <img alt="Nasi Boran" class="w-full rounded-lg mb-4 object-cover" style="height: 10%;" src="../../WebsiteOleh/img/boran.jpg" width="600"/>
  <p class="text-xs text-gray-600 mb-6">
   Sumber foto pinterest.
  </p>
  <p>
   Nasi boranan merupakan makanan khas Lamongan yang dikenal dengan penyajiannya yang unik dan otentik, yaitu nasi yang disajikan dalam wadah anyaman bambu (boran) dan dibungkus daun pisang atau koran. Nama nasi boran berasal dari wadah anyaman bambu yang digunakan untuk menyajikan nasi, yang disebut "boran". Selain nasi, nasi boran juga dilengkapi dengan berbagai lauk-pauk, seperti ayam, tahu, tempe, telur, hingga ikan-ikan khas Lamongan. 
  </p>
  <p class="mt-4">
   Nasi boran memiliki sejarah panjang di Lamongan, mulai dari dibuat untuk acara upacara desa atau hajatan hingga akhirnya dijajakan secara luas oleh para pedagang. Makanan ini menjadi daya tarik tersendiri bagi wisatawan dan masyarakat Lamongan karena rasa dan penyajiannya yang khas dan tradisional. Keunikan nasi boran terletak pada rasa bumbunya yang khas, serta penyajian yang masih menggunakan daun pisang dan koran, yang menambah kesan tradisional. Para pedagang nasi boran terus melestarikan dan mempertahankan makanan ini, sehingga nasi boran tetap menjadi ikon kuliner Lamongan. 
  </p>
  <p class="text-xs text-gray-600 mb-6">
